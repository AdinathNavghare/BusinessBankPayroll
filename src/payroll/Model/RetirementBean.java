package payroll.Model;

public class RetirementBean {
	
	private String date;
	private String retirmentDate;
	private String name;
	private String empcode;
	private int empno;
	private float basic;
	private float da;
	private float vda;
	private float amount;
	private float days;
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getRetirmentDate() {
		return retirmentDate;
	}
	public void setRetirmentDate(String retirmentDate) {
		this.retirmentDate = retirmentDate;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmpcode() {
		return empcode;
	}
	public void setEmpcode(String empcode) {
		this.empcode = empcode;
	}
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	public float getBasic() {
		return basic;
	}
	public void setBasic(float basic) {
		this.basic = basic;
	}
	public float getDa() {
		return da;
	}
	public void setDa(float da) {
		this.da = da;
	}
	public float getVda() {
		return vda;
	}
	public void setVda(float vda) {
		this.vda = vda;
	}
	public float getAmount() {
		return amount;
	}
	public void setAmount(float amount) {
		this.amount = amount;
	}
	public float getDays() {
		return days;
	}
	public void setDays(float days) {
		this.days = days;
	}
	
	
	

}
